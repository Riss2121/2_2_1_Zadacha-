// Объявление пакета, в котором находится интерфейс DAO (Data Access Object)
package hiber.dao;

// Импорт класса User — сущности, с которой будет работать DAO
import hiber.model.User;

// Импорт интерфейса List из java.util для работы с коллекциями
import java.util.List;

// Интерфейс UserDao определяет контракт для операций доступа к данным пользователей в базе данных
public interface UserDao {

   // Метод для добавления нового пользователя в базу данных
   // Принимает объект User в качестве параметра
   void add(User user);

   // Метод для получения списка всех пользователей из базы данных
   // Возвращает неизменяемый или изменяемый список (в зависимости от реализации) объектов User
   List<User> listUsers();

   // Метод для поиска пользователей по модели и серии автомобиля (предположительно, у User есть связь с Car)
   // Принимает:
   //   - model: строка, представляющая модель автомобиля (например, "Camry")
   //   - series: целое число, представляющее серию автомобиля (например, 2020)
   // Возвращает список пользователей, чьи автомобили соответствуют заданной модели и серии
   // Обратите внимание: в оригинале пробел перед угловой скобкой — это синтаксическая ошибка в Java.
   // Правильно: List<User>, а не List <User>
   List<User> userBySeriesAndModel(String model, int series);
}